public class recycada_data {
    String id, txt, isselsec;

    public recycada_data(String id, String txt, String isselsec) {
        this.id = id;
        this.txt = txt;
        this.isselsec = isselsec;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTxt() {
        return txt;
    }

    public void setTxt(String txt) {
        this.txt = txt;
    }

    public String getIsselsec() {
        return isselsec;
    }

    public void setIsselsec(String isselsec) {
        this.isselsec = isselsec;
    }
}
